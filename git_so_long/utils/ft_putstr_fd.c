#include "../so_long.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	j;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		j = s[i];
		write (fd, &j, 1);
		i++;
	}
}
